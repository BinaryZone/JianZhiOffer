package com.wh;
/**
 * 一只青蛙一次可以跳上1级台阶，也可以跳上2级。
 * 求该青蛙跳上一个n级的台阶总共有多少种跳法（先后次序不同算不同的结果）。
 * @author DELL
 *
 */
public class Jump {
	public int JumpFloor(int target) {
		if (target==0) {
			return 0;
		}
		int result = 0;
		if (target>0) {
			if (target==1) {
				return 1;
			}
			if (target==2) {
				return 2;
			}
			
			int preNum = 2;
			int prepreNum = 1;
			for(int i=2;i<target;i++) {
				result = preNum + prepreNum;
				prepreNum = preNum;
				preNum = result;
				
			}
			
		}
		return result;
    }
	
	public int JumpFloor01(int target) {
		if (target<=0) {
			return -1;
		}
		int result = 0;
		if (target>0) {
			if (target==1) {
				return 1;
			}
			if (target==2) {
				return 2;
			}
			result = JumpFloor(target-1) + JumpFloor(target-2);
		}
		return result;
    }
}
